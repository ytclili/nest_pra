/**
 * 用户角色枚举
 * 定义系统中的所有用户角色
 */
export enum UserRole {
  /**
   * 超级管理员 - 拥有所有权限
   * 可以管理其他管理员，修改系统配置
   */
  SUPER_ADMIN = "super_admin",

  /**
   * 管理员 - 拥有大部分管理权限
   * 可以管理用户、内容等，但不能管理其他管理员
   */
  ADMIN = "admin",

  /**
   * 版主 - 拥有内容管理权限
   * 可以管理内容、审核等，但不能管理用户
   */
  MODERATOR = "moderator",

  /**
   * 普通用户 - 基础权限
   * 只能访问和管理自己的内容
   */
  USER = "user",
}
